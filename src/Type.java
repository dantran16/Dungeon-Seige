/**
 * 
 * Name - Type
 * 
 * Description - This is used to apply a 
 * label to the gameobjects. By applying a type to
 * an object, it can change how it interacts with
 * other different types.
 */
public enum Type {
  Player(),
  Boss(),
  Ground(),
  Platform(),
  PlayerProjectile(),
  EnemyProjectile(),
  Wall();
}
